const languageButtons = document.querySelectorAll('[data-lang]');
const form = document.getElementById('reservation-form');
const status = document.getElementById('form-status');
const dateInput = form.querySelector('input[name="date"]');
let currentLanguage = localStorage.getItem('meeNoodleLanguage') || 'en';

function t(key) {
  return translations[currentLanguage][key] || translations.en[key] || key;
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : 'en';
  document.documentElement.lang = currentLanguage;
  document.title = currentLanguage === 'nl' ? 'Mee Noodle - Amsterdam' : 'Mee Noodle - Amsterdam';

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach((element) => {
    element.innerHTML = t(element.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-placeholder-key]').forEach((element) => {
    element.placeholder = t(element.dataset.placeholderKey);
  });

  languageButtons.forEach((button) => {
    const active = button.dataset.lang === currentLanguage;
    button.classList.toggle('active', active);
    button.setAttribute('aria-pressed', String(active));
  });

  localStorage.setItem('meeNoodleLanguage', currentLanguage);
}

function getTodayIsoDate() {
  const today = new Date();
  const year = today.getFullYear();
  const month = String(today.getMonth() + 1).padStart(2, '0');
  const day = String(today.getDate()).padStart(2, '0');

  return `${year}-${month}-${day}`;
}

function setDateConstraints() {
  dateInput.min = getTodayIsoDate();
}

languageButtons.forEach((button) => {
  button.addEventListener('click', () => {
    applyLanguage(button.dataset.lang);
  });
});

applyLanguage(currentLanguage);
setDateConstraints();

form.addEventListener('submit', async (event) => {
  event.preventDefault();
  status.style.color = '';
  setDateConstraints();

  if (dateInput.value && dateInput.value < dateInput.min) {
    status.textContent = t('dateError');
    status.style.color = '#b23a2d';
    dateInput.focus();
    return;
  }

  status.textContent = t('sending');

  const formData = new FormData(form);
  const payload = Object.fromEntries(formData.entries());

  try {
    const response = await fetch('/api/reservations', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });

    if (!response.ok) {
      const error = await response.json();
      throw new Error(error.error || t('defaultError'));
    }

    status.textContent = t('reservationSuccess');
    form.reset();
    setDateConstraints();
  } catch (error) {
    console.error('Fetch error:', error);
    status.textContent = `${t('reservationError')}: ${error.message}`;
    status.style.color = '#b23a2d';
  }
});
